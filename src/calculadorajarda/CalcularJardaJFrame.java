    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        double jarda;
        double metro;
        double conversaoMetro;
        double conversaoJarda;
        
        
        
        if (RadioBtnYdToM.isSelected()){
         txtMetro1.setText("");
         jarda=Double.parseDouble(txtJarda1.getText());
         conversaoJarda= jarda*0.9144;
         JOptionPane.showMessageDialog(null, "Resultado " + conversaoJarda + " Metros(s)");
        }
        else{
         txtJarda1.setText("");   
         metro=Double.parseDouble(txtMetro1.getText());
         conversaoMetro= metro/0.9144;
         JOptionPane.showMessageDialog(null,"Resultado " + conversaoMetro + " Jarda(s)");
         
        }
        
        
    }//GEN-LAST:event_btnCalcularActionPerformed

